package com.example.demo22.Model;

import jakarta.persistence.*;

@Entity
@Table(name = "accounts")
public class Accounts { // Updated class name to 'Accounts'
    @Id
    @SequenceGenerator(
            name = "accounts_id_sequence",
            sequenceName="accounts_id_sequence"
    )
    @GeneratedValue(strategy = GenerationType.IDENTITY) // Match the ID generation strategy of your database
    private int id;

    @Column(nullable = false, unique = true)
    private String username;

    @Column(nullable = false)
    private String password;

    @Column(nullable = false, unique = true)
    private String email;

    private String fullname;

    private String phone;

    private int status;

    // Getters and setters...
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
